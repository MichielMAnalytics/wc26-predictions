#!/usr/bin/env python3
"""Render SCORITO_PREDICTIONS.md from the prediction CSVs + simulation probs."""
import csv, json
gm=list(csv.DictReader(open("data/predictions/group_matches.csv")))
st=list(csv.DictReader(open("data/predictions/group_standings.csv")))
ko=list(csv.DictReader(open("data/predictions/knockout.csv")))
sim={r["team"]:r for r in csv.DictReader(open("data/model/sim_team_probs.csv"))}
summ=json.load(open("data/predictions/_summary.json"))

L=[]
L.append("# Scorito WK 2026 — model predictions\n")
L.append("Generated by the Dixon-Coles + Monte-Carlo pipeline (see [MODEL.md](MODEL.md)). "
         "Every predicted scoreline is the **expected-points-optimal** Scorito pick "
         "(`argmax 30·P(outcome)+15·P(exact)`), per [SCORITO_RULES.md](SCORITO_RULES.md).\n")
L.append(f"**Champion pick: {summ['champion']}** · Runner-up: {summ['runner']} · 3rd place: {summ['third']}\n")
exp=sum(float(r['exp_points']) for r in gm)
L.append(f"Expected group-stage points from these 72 picks: **~{exp:.0f}** "
         "(plus knockout, standings and champion points on top).\n")

L.append("## Title odds (30,000-tournament Monte Carlo)\n")
L.append("| team | win group→ R32 | reach QF | reach SF | final | **champion** |")
L.append("|---|---|---|---|---|---|")
for t,r in sorted(sim.items(), key=lambda x:-float(x[1]["p_champion"]))[:16]:
    L.append(f"| {t} | {float(r['p_advance_R32'])*100:.0f}% | {float(r['p_reach_QF'])*100:.0f}% | "
             f"{float(r['p_reach_SF'])*100:.0f}% | {float(r['p_final'])*100:.0f}% | **{float(r['p_champion'])*100:.1f}%** |")

L.append("\n## Group-stage score predictions\n")
import itertools
for g, rows in itertools.groupby(gm, key=lambda r:r["group"]):
    rows=list(rows)
    L.append(f"\n**Group {g}**\n")
    L.append("| match | pick | W / D / L |")
    L.append("|---|---|---|")
    for r in rows:
        L.append(f"| {r['home']} – {r['away']} | **{r['pred']}** | "
                 f"{float(r['p_home'])*100:.0f}/{float(r['p_draw'])*100:.0f}/{float(r['p_away'])*100:.0f} |")
    # standings for this group
    gs=[s for s in st if s["group"]==g]
    adv=" · ".join(f"{s['position']}. {s['team']} ({s['pts']}p)" for s in gs)
    L.append(f"\nPredicted standings: {adv}")

L.append("\n## Knockout bracket predictions\n")
L.append("| round | match | pick | advances |")
L.append("|---|---|---|---|")
for r in ko:
    L.append(f"| {r['round']} | {r['home']} – {r['away']} | {r['pred']} | **{r['advances']}** |")

L.append("\n## Topscorer suggestions (heuristic)\n")
L.append("Forwards/mids from teams the sim sends deep, weighted by international scoring rate. "
         "Note Scorito pays more per goal for defenders/midfielders (32/16/8) — a contrarian "
         "mid/defender from a strong side can out-score a forward pick.\n")
L.append(", ".join(f"{p['player']} ({p['team']})" for p in summ["topscorers"][:10]))

L.append("\n## How to use / caveats\n")
L.append("- Scores are tuned to **maximise Scorito expected points**, not to look realistic: "
         "many 1-0/0-1 picks are correct because the outcome (toto, 30 pts) dominates and a "
         "minimal-margin favourite win is the single most-likely exact score.\n")
L.append("- Backtest skill (see MODEL.md): ~26% RPS improvement over climatology, 60% outcome "
         "accuracy out-of-sample; tournaments are high-variance so treat deep-run picks as edges, not certainties.\n")
L.append("- Confirm Scorito's exact point values in-app before submitting; penalty shootouts don't count toward scores.\n")

open("SCORITO_PREDICTIONS.md","w",encoding="utf-8").write("\n".join(L)+"\n")
print("wrote SCORITO_PREDICTIONS.md")
